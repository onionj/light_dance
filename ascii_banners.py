mode = 1
mode_count = 0


def banner():
    '''print random ascii banner'''

    global mode
    global mode_count

    if mode_count == 12:
        mode = 2
    elif mode_count >= 33:
        mode = 1
        mode_count = 1

    if mode == 1:
        banner_mode_1()
    elif mode == 2:
        banner_mode_2()

    mode_count += 1
